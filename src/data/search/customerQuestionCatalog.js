export const MATCH_QUESTIONS = CUSTOMER_QUESTIONS.filter((q) => q.scope === 'match');

/** Nur vollständig beantwortbare Matching-Fragen */
export const FULLY_ANSWERABLE = MATCH_QUESTIONS.filter((q) => q.status === 'full');

export function getQuestionsByCategory(categoryId) {
  return CUSTOMER_QUESTIONS.filter((q) => q.category === categoryId);
}

export function getQuestionById(id) {
  return CUSTOMER_QUESTIONS.find((q) => q.id === id) ?? null;
}

/** Abdeckungsstatistik für Dashboard / Sprint-Tracking */
export function getQuestionCoverageStats() {
  const match = MATCH_QUESTIONS;
  const full = match.filter((q) => q.status === 'full').length;
  const partial = match.filter((q) => q.status === 'partial').length;
  const missing = match.filter((q) => q.status === 'missing').length;
  const highPriority = match.filter((q) => q.priority === 'high');
  const highMissing = highPriority.filter((q) => q.status === 'missing' || q.status === 'partial');

  return {
    total: CUSTOMER_QUESTIONS.length,
    matchTotal: match.length,
    beratungTotal: CUSTOMER_QUESTIONS.length - match.length,
    full,
    partial,
    missing,
    matchCoveragePercent: match.length ? Math.round((full / match.length) * 100) : 0,
    highPriorityTotal: highPriority.length,
    highPriorityGaps: highMissing.map((q) => q.id),
  };
}

